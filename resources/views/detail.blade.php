                <div class="big-text3">
                    <h3>How do you describe the architecture and interior?</h3>
                    <p>
                        One of the interesting things is the fact that we avoided the obvious. We could have gone down that route; you know, the
                        ‘fado, the ‘azulejos’… the typical Portuguese characteristics. We wanted it to feel like your house
                        in Alfama, not the museum of Alfama. We wanted our guests to discover the authenticity through the
                        location. We decided to go down the contemporary route, which is unexpected.
                    </p>
                    <p>
                        You can already hear the fado in the streets, therefore we don’t need a big photo of Amalia Rodrigues (the most famous Portuguese
                        Fado artist) on the wall. We also wanted to tell the story behind the building – for example, we
                        have this photo on display which looks like two sculptures dancing, which was taken by the celebrated
                        photographer Alexandre Coque. The photo itself was taken here, where his studio was based.
                    </p>
                    <p>
                        We have a mural of a working class local man which was created by the street artist
                        <a href="" class="external-link">Vhils</a> who also created a mural for us in Baleeira. We want to be unique without being pretentious.
                    </p>
                    <p>
                        The architect, Samuel Torres de Carvalho, did a superb job in creating coherence between the two original buildings. The
                        buildings were totally abandoned and I didn’t think it would be possible to create a hotel out of
                        the space. It was a team effort – my partner João and Samuel, were key in making the concept a reality.
                    </p>
                    <h3>Please tell us about the rooms in the hotel.</h3>
                    <p>
                        The rooms are small because everything in Alfama is small. The concept has always been ‘to create your house in Alfama’.
                        Comfort is our number one priority – everything from the materials we have used to the beds and duvets.
                        Each bed has 6 pillows. The colour palette is quite warm so it feels more like your own room than
                        a hotel room. We have a strong focus on Portuguese products – in the mini bar we have Vidago water
                        not Perrier and Super Bock, not Heineken.
                    </p>
                    <h3>Tell us something unusual about this neighbourhood.</h3>
                    <p>
                        What I love the most about walking around the streets is that it feels like going back in time – how it was to live behind
                        the castle walls, on narrow streets with the neighbours talking from one window to another. Pretty
                        much everywhere else, this feeling has been lost. Here locals still know each others’ names, whom
                        married who and did what. It’s a big contrast with downtown Lisbon. Even if you’re new to the neighbourhood,
                        people talk to you and invite you over. People make you feel welcome and you become part of the Alfama
                        family.
                    </p>
