                    <p>
                        No, that’s not totally true. We already had the approval to build the swimming pool. It was Samuel’s idea because you can
                        see the hotel from one of the main view points here in Alfama – Mirador de Santa Luzia. We didn’t
                        want to break the coherence of the view. Even when you fly you can always notice swimming pools from
                        the plane and we didn’t want that. The idea was for the swimming pool to blend in with the scenery
                        as much as possible, not to distract from it.
                    </p>
