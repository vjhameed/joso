                    <p>
                        It has – I grew up with hotels. My grandfather founded the Tivoli Hotels and my dad also always worked with hotels. I studied
                        Business Management but not with the aim of working in hotels but once I worked at the Four Seasons,
                        I realised, “this is what I want to do!”.
                    </p>
                    <p>Back then the Managing Director of a hotel was more like a PR person but I have always loved marketing
                        and creative concepts. It’s very important to know how to host people but I also love telling a story.
                        I wanted to create a project for travellers, not for tourists. For me the traveller is that person
                        that wants to try new things and that’s what captivates me. People can feel when there’s a passion
                        behind what you do.</p>
