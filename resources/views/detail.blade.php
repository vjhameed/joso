                <div class="big-text3">
                    <p>
                        Eight years ago, I challenged João Corrêa Nunes, my business partner and co-founder, to work out how we could kick off the
                        Memmo project. The first thing we invested in was creating a brand. From the very beginning our aim
                        was to be able to tell a story, and to have a mission and a soul.
                    </p>
                    <p>
                        Our logo, the ‘chameleon’ represents how we want to adapt the hotels to their environment. Our brand has a strong focus on
                        our 3 core values: hospitality, great service, and remaining contemporary. We also focus on originality,
                        which for me means doing the same thing in different ways. These aims are true for all of the Memmo
                        collection hotels.
                    </p>
                    <h3>
                        Was the original plan always for the hotel to be in Alfama?
                    </h3>
                    <p>
                        We picked Alfama because it is, in our opinion, Lisbon’s most authentic neighbourhood. At the beginning we weren’t sure about
                        the exact concept for the hotel – all we knew was that we wanted to be in Alfama. We decided to be
                        there seven years prior to finding the building, one year after opening
                        <a href="" class="external-link">Memmo Baleeira</a>.
                    </p>
                    <p>
                        Our ambition is to showcase the most authentic aspects of each destination. Our tagline is “Unforgettable Hotels” so if you’re
                        going to stay at Memmo Alfama the statement we are making is “You’ll never forget the authentic Lisbon”.
                    </p>
                    <p>
                        We felt it was going to be a challenge to build a hotel in a hidden cobbled street. It had everything we were looking for
                        including the surprise factor – which is a reflection of Alfama itself. At first you walk into a
                        new street without any expectation and then all of a sudden you come across an amazing view over
                        the city. It makes you feel like you’re a bird.
                    </p>

